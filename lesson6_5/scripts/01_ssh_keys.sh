#!/bin/bash
# =============================================================
#  01_ssh_keys.sh — Демонстрація генерації SSH-ключів
#  Лабораторна робота: PKI | ВІТІ
# =============================================================

set -e

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
RED='\033[0;31m'
NC='\033[0m' # No Color

header() { echo -e "\n${CYAN}══════════════════════════════════════════${NC}"; echo -e "${CYAN}  $1${NC}"; echo -e "${CYAN}══════════════════════════════════════════${NC}"; }
ok()     { echo -e "${GREEN}  ✔ $1${NC}"; }
info()   { echo -e "${YELLOW}  ℹ $1${NC}"; }
err()    { echo -e "${RED}  ✘ $1${NC}"; }

# ------------------------------------------------------------
header "КРОК 1 — Перевірка мережі"
# ------------------------------------------------------------

info "Отримані IP-адреси:"
ip -4 a show | grep inet | awk '{print "     " $2}'

info "Шлюз за замовчуванням:"
ip route show default | awk '{print "     " $3}'

read -rp $'\n  Введіть IP вашої VM: ' VM_IP
ping -c 2 "$VM_IP" &>/dev/null && ok "VM $VM_IP доступна" || err "VM $VM_IP недоступна"

# ------------------------------------------------------------
header "КРОК 2 — Генерація пари SSH-ключів (ED25519)"
# ------------------------------------------------------------

KEY_DIR="$HOME/.ssh"
KEY_FILE="$KEY_DIR/id_ed25519_lab"

mkdir -p "$KEY_DIR"
chmod 700 "$KEY_DIR"

if [[ -f "$KEY_FILE" ]]; then
    info "Ключ $KEY_FILE вже існує — пропускаємо генерацію"
else
    # Генерація ключа без passphrase (для лабораторії)
    ssh-keygen -t ed25519 \
               -C "cadet@tsa233.lab" \
               -f "$KEY_FILE" \
               -N ""           # порожній passphrase

    ok "Ключі згенеровано:"
    echo -e "     Приватний: ${RED}$KEY_FILE${NC}  ← нікому не передавати!"
    echo -e "     Публічний: ${GREEN}${KEY_FILE}.pub${NC}  ← можна роздавати"
fi

# ------------------------------------------------------------
header "КРОК 3 — Перегляд публічного ключа"
# ------------------------------------------------------------

info "Вміст публічного ключа:"
cat "${KEY_FILE}.pub"

info "Відбиток ключа (fingerprint):"
ssh-keygen -lf "${KEY_FILE}.pub"

# ------------------------------------------------------------
header "КРОК 4 — Додавання ключа до authorized_keys"
# ------------------------------------------------------------

AUTH_FILE="$KEY_DIR/authorized_keys"

# Перевірити чи ключ вже є
if grep -qF "$(cat "${KEY_FILE}.pub")" "$AUTH_FILE" 2>/dev/null; then
    info "Ключ вже є в authorized_keys"
else
    cat "${KEY_FILE}.pub" >> "$AUTH_FILE"
    chmod 600 "$AUTH_FILE"
    ok "Ключ додано до $AUTH_FILE"
fi

info "Вміст authorized_keys:"
cat "$AUTH_FILE"

# ------------------------------------------------------------
header "КРОК 5 — Тест підключення по ключу"
# ------------------------------------------------------------

info "Спроба підключення до localhost по ключу..."
ssh -i "$KEY_FILE" \
    -o StrictHostKeyChecking=no \
    -o PasswordAuthentication=no \
    -o ConnectTimeout=5 \
    localhost "echo '  Підключення по ключу успішне!'" \
    && ok "Автентифікація по ключу працює" \
    || err "Підключення не вдалось — перевірте чи запущено sshd"

# ------------------------------------------------------------
header "РЕЗУЛЬТАТ"
# ------------------------------------------------------------

echo ""
echo -e "  Публічний ключ для передачі адміністратору:"
echo ""
cat "${KEY_FILE}.pub"
echo ""
ok "Скрипт виконано успішно"
echo ""
info "Щоб підключитись до іншого сервера з цим ключем:"
echo "     ssh -i $KEY_FILE USER@HOST"
